public class StringsAndConcatenations {

    public static void main(String[] s) {

        String firstname = "Sadikshya";
        String space = " ";
        String lastname = "Acharya";

        System.out.println(firstname+space+lastname);
        System.out.println(firstname + lastname);
        System.out.println(firstname+ "" + lastname);
        System.out.println(firstname+ " " + lastname);

  }

}
